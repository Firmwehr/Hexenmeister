class CsePhi {
        public static void main(String[] args) {
                int i = 0;
                int j = 0;
                int z = 0;
                while (i < 5) {
                        i = i + 1;
                        j = j + 1;
                        z = i + j;
                }
                System.out.println(z);
        }
}
